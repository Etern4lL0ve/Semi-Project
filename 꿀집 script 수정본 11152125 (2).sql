CREATE TABLE MEMBER (
    MEMBER_NO NUMBER PRIMARY KEY,
    MEMBER_ID VARCHAR(30) NOT NULL UNIQUE,
    MEMBER_PWD VARCHAR(100) NOT NULL,
    MEMBER_NAME VARCHAR(20) NOT NULL,
    MEMBER_NICKNAME VARCHAR(40) NOT NULL UNIQUE,
    MEMBER_EMAIL VARCHAR(100) NOT NULL,
    MEMBER_ENROLL_DATE DATE DEFAULT SYSDATE,
    MEMBER_GRADE  VARCHAR(20) DEFAULT '일반회원',
    STATUS  VARCHAR2(1) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N'))
);
COMMENT ON COLUMN MEMBER.MEMBER_NO IS '회원번호';
COMMENT ON COLUMN MEMBER.MEMBER_ID IS '회원아이디';
COMMENT ON COLUMN MEMBER.MEMBER_PWD IS '회원비밀번호';
COMMENT ON COLUMN MEMBER.MEMBER_NAME IS '회원명';
COMMENT ON COLUMN MEMBER.MEMBER_NICKNAME IS '별명';
COMMENT ON COLUMN MEMBER.MEMBER_EMAIL IS '이메일';
COMMENT ON COLUMN MEMBER.MEMBER_ENROLL_DATE IS '회원가입일';
COMMENT ON COLUMN MEMBER.MEMBER_GRADE IS '회원등급';
COMMENT ON COLUMN MEMBER.STATUS IS '상태값(Y/N)';
